package com.emmett.auto_check.constants;

public enum ApiResultEnum {
    SUCCESS("200","请求成功"),
    FAILED("999","驳回");

    ApiResultEnum(String code, String name) {
        this.code = code;
        this.name = name;
    }

    private String code;
    private String name;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
